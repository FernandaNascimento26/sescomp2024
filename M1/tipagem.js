
/*Em JavaScript, você pode declarar uma variável e atribuir valores de qualquer tipo a ela, sem restrições. 

Por exemplo, podemos definir uma variável como let x = 5,
e depois, em outro ponto do código, atribuir uma string a ela, como x = 'Hello'. 

O JavaScript não vai impedir isso.*/

let x = 5;        // x é um número
x = "Hello";      // x agora é uma string
console.log(x);   // Saída: Hello
